<?php
header('Content-Type:text/html; charset=utf-8');

/*
假设某人有100,000现金
每经过一次路口需要进行一次交费。
交费规则为当他现金大于50,000每次需要交5%如果现金小于等于50,000时每次交5,000.
请写一程序计算些人可以经过多少次这个路口
*/

for($i=100000, $count=0; $i>=5000;){
	$count ++;
	if($i > 50000){
		$i*=0.95;
	}else{
		$i-=5000;
	}

	echo '<h3>过第',$count,'次路口还剩',$i,'元钱</h3>';

}
?>
